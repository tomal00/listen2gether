export const requestDeleteRoom = () => ({ type: REQUEST_DELETE_ROOM });
export const requestLeaveRoom = () => ({ type: REQUEST_LEAVE_ROOM });
export const requestPlaylistAddition = (trackUri) => ({ type: REQUEST_TRACK_ADDITION, trackUri });
export const requestPlaylistRemoval = (trackUri) => ({ type: REQUEST_TRACK_REMOVAL, trackUri });
export const requestKickClient = (clientId) => ({ type: REQUEST_KICK_CLIENT, clientId });
