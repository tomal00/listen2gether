export const REQUEST_CHANGE_NICK = 'REQUEST_CHANGE_NICK';
export const SEND_CHAT_MESSAGE = 'SEND_CHAT_MESSAGE';
export const SEND_DEVICE_ID = 'SEND_DEVICE_ID';
export const REQUEST_PLAY_TRACK = 'REQUEST_PLAY_TRACK';
export const REQUEST_UNPAUSE = 'REQUEST_UNPAUSE';
export const REQUEST_PAUSE = 'REQUEST_PAUSE';
export const REQUEST_MOVE_PLAYBACK = 'REQUEST_MOVE_PLAYBACK';
export const REQUEST_JOIN_ROOM = 'REQUEST_JOIN_ROOM';
export const ALLOW_SEEKING = 'ALLOW_SEEKING';
export const REQUEST_CREATE_ROOM = 'REQUEST_CREATE_ROOM';
export const REQUEST_DELETE_ROOM = 'REQUEST_DELETE_ROOM';
export const REQUEST_LEAVE_ROOM = 'REQUEST_LEAVE_ROOM';
export const REQUEST_TRACK_ADDITION = 'REQUEST_TRACK_ADDITION';
export const REQUEST_TRACK_REMOVAL = 'REQUEST_TRACK_REMOVAL';
