export const requestPlayTrack = (trackUri) => ({ type: REQUEST_PLAY_TRACK, trackUri });
export const requestUnpause = () => ({ type: REQUEST_UNPAUSE });
export const requestPause = () => ({ type: REQUEST_PAUSE });
export const requestMovePlayback = (time) => ({ type: REQUEST_MOVE_PLAYBACK, time });
export const requestJoinRoom = (roomId, password) => ({ type: REQUEST_JOIN_ROOM, roomId, password });
export const allowSeeking = () => ({ type: ALLOW_SEEKING });
